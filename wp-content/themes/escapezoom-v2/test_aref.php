$start_timestamp = strtotime($start_date);

// تاریخ پایان: 2026-04-20 23:59:59
$end_date = "2026-04-20 23:59:59";
$end_timestamp = strtotime($end_date);

// زمان فعلی برای ثبت در booked_time
$current_time = time();

echo "شروع فرآیند بستن سانس‌ها از تاریخ $start_date تا $end_date ...\n";

// ۳. دریافت لیست تمام بازی‌هایی که مقدار active آن‌ها برابر با 'active' است
$active_products = $database->select("products_data", [
    "product_id",
    "schedule"
], [
    "active" => "active"
]);

$total_closed = 0;

// ۴. بررسی هر محصول و سانس‌های آن
foreach ($active_products as $product) {
    $product_id = $product['product_id'];
    $schedule_data = $product['schedule'];
    
    // آن‌سریالایز کردن داده‌های schedule
    $schedule = unserialize($schedule_data);

    // بررسی اینکه آیا آبجکت معتبر است
    if (is_object($schedule)) {
        
        // ۵. حلقه روی تمام روزهای داخل بازه زمانی
        $day_timestamp = $start_timestamp;
        
        while ($day_timestamp <= $end_timestamp) {
            
            // دریافت نام روز به فارسی یا انگلیسی (Sat, Sun, Mon, Tue, Wed, Thu, Fri)
            // تابع date('D') خروجی مثل 'Sat' می‌دهد
            $day_name = date('D', $day_timestamp);
            
            // تعیین نوع سانس بر اساس روز
            // شنبه تا چهارشنبه = normals
            // پنجشنبه و جمعه = holidays
            if (in_array($day_name, ['Sat', 'Sun', 'Mon', 'Tue', 'Wed'])) {
                $schedule_type = 'normals';
            } else {
                // پنجشنبه (Thu) و جمعه (Fri)
                $schedule_type = 'holidays';
            }

            // بررسی وجود این نوع سانس در آبجکت
            if (isset($schedule->$schedule_type) && is_array($schedule->$schedule_type)) {
                
                foreach ($schedule->$schedule_type as $sans) {
                    // دریافت زمان سانس (مثلاً "16:00")
                    $sans_hour = $sans->time;
                    
                    // ساخت تایم استمپ کامل: تاریخ روز + ساعت سانس
                    $full_sans_timestamp = strtotime(date('Y-m-d', $day_timestamp) . ' ' . $sans_hour);
                    
                    // بررسی تکراری نبودن
                    $exists = $database->has("wp_zb_booking_history", [
                        "AND" => [
                            "room_id" => $product_id,
                            "booking_time" => $full_sans_timestamp,
                            "status" => 2
                        ]
                    ]);

                    if (!$exists) {
                        // درج رکورد بسته شدن سانس
                        $database->insert("wp_zb_booking_history", [
                            "customer_id" => 0,
                            "wc_order_id" => null,
                            "status" => 2,
                            "room_id" => $product_id,
                            "booking_time" => $full_sans_timestamp,
                            "booked_time" => $current_time,
                            "name" => null,
                            "phone" => null,
                            "quantity" => 0
                        ]);

                        $total_closed++;
                        echo "سانس با زمان " . date('Y-m-d H:i:s', $full_sans_timestamp) . " ($schedule_type) برای محصول ID $product_id بسته شد.\n";
                    }
                }
            }
            
            // رفتن به روز بعد
            $day_timestamp += 86400;
        }
    }
