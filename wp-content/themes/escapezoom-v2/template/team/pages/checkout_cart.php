<script>
jQuery(function($) {
	function ezNotify(msg, ok) {
		var $e = $('#ez_checkout_cart_error');
		$e.toggleClass('text-red-600', !ok).toggleClass('text-green-700', !!ok).text(msg).removeClass('hidden');
		setTimeout(function() { $e.addClass('hidden'); }, ok ? 4000 : 8000);
	}
	function ezLoadCheckoutIntents() {
		var ajaxUrl = '<?php echo esc_url(admin_url('admin-ajax.php')); ?>';
		var nonce = '<?php echo esc_js(wp_create_nonce('team-ajax-nonce')); ?>';

		$('#ez_checkout_cart_loading').removeClass('hidden');
		$('#ez_checkout_cart_error').addClass('hidden');

		$.post(ajaxUrl, {
			action: 'team_ajax_handler',
			nonce: nonce,
			callback: 'checkout_intents_list'
		}).done(function(res) {
			$('#ez_checkout_cart_loading').addClass('hidden');
			var $tb = $('#ez_checkout_cart_tbody').empty();

			if (!res || !res.success || !res.data || !Array.isArray(res.data.rows)) {
				ezNotify((res && res.data) ? res.data : 'پاسخ نامعتبر', false);
				$tb.append('<tr><td colspan="8" class="p-6 text-center">خطا</td></tr>');
				return;
			}

			if (res.data.rows.length === 0) {
				$tb.append('<tr><td colspan="8" class="p-6 text-center text-gray-500">سبد بازی وجود ندارد.</td></tr>');
				return;
			}

			res.data.rows.forEach(function(r) {
				var uid = r.user_id ? parseInt(r.user_id, 10) : 0;
				var phone = r.user_phone || '';
				var canSms = uid > 0 && phone !== '';
				var rowId = parseInt(r.id, 10) || 0;
				var smsSent = r.sms_reminder_sent_at != null && String(r.sms_reminder_sent_at).trim() !== '';

				var tr = $('<tr/>').addClass('border-t border-[#E4EBF0] hover:bg-[#FAFDFF]');
				tr.append('<td class="p-3 whitespace-nowrap">' + (r.id || '') + '</td>');
				tr.append('<td class="p-3 whitespace-nowrap">' + (r.updated_at || '') + '</td>');
				tr.append('<td class="p-3">' + (r.user_display || (uid ? ('#' + uid) : '—')) + '</td>');
				tr.append('<td class="p-3" dir="ltr">' + (phone || '—') + '</td>');
				tr.append('<td class="p-3">' + $('<div/>').text(r.product_title || '').html() + '</td>');
				tr.append('<td class="p-3 font-mono" dir="ltr">' + (r.sans_ts || '') + '</td>');
				tr.append('<td class="p-3">' + (r.qty || '') + '</td>');

				if (smsSent) {
					tr.append('<td class="p-3"><span class="inline-flex items-center gap-1 text-green-700 text-xs font-yekan-bold whitespace-nowrap" title="ارسال شد">✓ ارسال شد</span></td>');
				} else {
					var $btn = $('<button type="button" class="rounded-lg bg-[#1447E6] px-3 py-1.5 text-xs text-white disabled:opacity-50">پیامک</button>');
					if (!canSms) $btn.prop('disabled', true);
					$btn.on('click', function() {
						if (!canSms || !rowId) return;
						$btn.prop('disabled', true);
						$.post(ajaxUrl, {
							action: 'team_ajax_handler',
							nonce: nonce,
							callback: 'checkout_intent_sms_remind',
							row_id: rowId
						}).done(function(r2) {
							if (r2.success) {
								ezNotify(r2.data || 'ارسال شد', true);
								ezLoadCheckoutIntents();
							} else {
								ezNotify(r2.data || 'خطا', false);
							}
						}).fail(function() {
							ezNotify('خطای شبکه', false);
						}).always(function() {
							if (canSms) $btn.prop('disabled', false);
						});
					});
					tr.append($('<td class="p-3"/>').append($btn));
				}

				$tb.append(tr);
			});
		}).fail(function() {
			$('#ez_checkout_cart_loading').addClass('hidden');
			ezNotify('خطا در ارتباط با سرور', false);
		});
	}

	$('#ez_checkout_cart_reload').on('click', ezLoadCheckoutIntents);
	ezLoadCheckoutIntents();
});
</script>
