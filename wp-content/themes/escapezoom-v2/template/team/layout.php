                var $resultBox = $('#admin-otp-result');

                // مخفی کردن پیام‌های قبلی
                $errorBox.hide();
                $resultBox.hide();

                // اعتبارسنجی سمت کلاینت
                var mobileRegex = /^(\+98|0|0098)?9\d{9}$/;
                
                if (!mobile) {
                    $errorBox.text('شماره موبایل ضروری میباشد').show();
                    return;
                }
                if (!/^\d+$/.test(mobile)) {
                    $errorBox.text('شماره موبایل صحیح نیست').show();
                    return;
                }
                if (!mobileRegex.test(mobile)) {
                    $errorBox.text('شماره موبایل صحیح نیست').show();
                    return;
                }

                // تغییر وضعیت دکمه
                $btn.prop('disabled', true).text('در حال جستجو...');

                // ارسال درخواست AJAX طبق الگوی درخواستی
                $.ajax({
                    type: 'POST',
                    url: "<?php echo admin_url('admin-ajax.php'); ?>",
                    data: {
                        'action': 'team_ajax_handler',
                        'nonce': "<?php echo wp_create_nonce('team-ajax-nonce'); ?>",
                        'callback': 'otp_by_admin',
                        'phone': mobile
                    },
                    success: function(response) {
                        if (response.success) {
                            // نمایش کد
                            $('#admin-otp-code-display').text(response.data.otp);
                            $resultBox.show();
                        } else {
                            // نمایش خطا
                            $errorBox.text(response.data || 'خطایی رخ داد').show();
                        }
                    },
                    error: function() {
                        $errorBox.text('خطا در ارتباط با سرور').show();
                    },
                    complete: function() {
                        // بازگرداندن وضعیت دکمه
                        $btn.prop('disabled', false).text('دریافت کد');
                    }
                });
            });

